
public class TestandMath {

	public static void main(String[] args) {
		
		// Math é uma classe que contém diversos métodos
		System.out.println("O número randomizado é: " + (int) (Math.random() * 1000));
		System.out.println(Math.min(10, 8));
		System.out.println(Math.max(560, 20));
		System.out.println(Math.negateExact(20));
		System.out.println(Math.sqrt(12.5));

	}

}
